	public function createUser($datos){

		$user = $this->find("first",array("conditions" => array("emailAddress" => $datos["emailAddress"])));

		if (!empty($user)) {
			$this->id = $user["User"]["id"];
			$datos["id"] = $user["User"]["id"];
		}else{
			$this->create();
		}

		$this->save(array("User"=>$datos));

		return $this->id;

	}

